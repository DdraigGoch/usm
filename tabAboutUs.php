<p class="pageName">About Us</p>
<p align="justify" class="style1">The United Services Mess was
founded at its present address, Westminster Chambers, Wharton
Street Cardiff in 1909.</p>
<p align="justify" class="style1">The Mess was founded by Senior
NCOs from Maindy Barracks but included in their number, it is
pleasing to note, was one T.D. Lucas, a Naval gunnery instructor.
The Royal Flying Corps, the Royal Naval Service and the Royal Air
Force did not exist at this time. They were admitted later, as was
the Merchant Navy.</p>
<p align="justify" class="style1">The object of the founder members
was to take the concept of a Services Mess outside the confines of
a military base and preserve its unique qualities for themselves
and future members. Today service in the Armed Forces is not a
prerequisite for membership. Service Mess etiquette, comradeship
and entertainment are reflected in U.S.M.</p>
<p align="justify" class="style1">Some items of historical interest
of which we are justly proud. Our first President in 1909 was Lord
Tredegar who took part in the famous charge of the Light Brigade at
Balaclava during the Crimean War. On 5th February 1927, the Mess
was visited by HRH The Prince of Wales. Seemingly he visited the
Mess on several occasions when he came to Cardiff to attend Rugby
Internationals at The Arms Park.</p> 
<p align="justify" class="style1">In 1951 a letter was written by
the Lord Mayor of Cardiff, soliciting a visit by King George VI
indicated that there were in excess of 900 members who had given
military service and among that number were fourteen holders of the
Victoria Cross.</p>
