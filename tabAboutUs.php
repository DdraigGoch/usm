<p align="justify" class="style1">On 23rd March 1994 a Memorial
Tablet to Members who gave their lives in two World Wars and
subsequent conflicts, was dedicated at the Mess Church of St. Johns
the Baptist's by the then Mess Chaplain, The Reverend Canon Malcolm
Ellis O St J, in the presence of HRH Prince Andrew, The Duke of
York. He visited the Mess after the service of lunch and spent time
talking to Mess Members and in 2005 the Duke of York was the Guest
Of Honour at the 90th Annual Dinner of the Mess at The Angel Hotel,
Cardiff. He visited the Mess afterwards.</p>
<p align="justify" class="style1">In January 2009, a dinner was
held at The Angel Hotel to celebrate the centenary of the Mess.</p>
<div class="pageName">The Mess Today</div>
<p align="justify" class="style1">Membership is currently around
500. Country membership accounts for about a fifth of the
total.</p>
